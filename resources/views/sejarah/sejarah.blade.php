                    I. Keputusan Gubernur Kalbar No.49/2006 tanggal 15-2-2006 tentang Pemberian Dukungan Dana
                    Operasional bagi Penyelenggaraan Pemerintah Kabupaten Kubu Raya yang akan dibentuk di Propinsi
                    Kalimantan Barat.
                </p>
